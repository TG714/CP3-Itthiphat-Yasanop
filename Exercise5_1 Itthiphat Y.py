num_1 = int(input("The 1st number: "))
num_2 = int(input("The 2nd number: "))
plus = num_1 + num_2
minus = num_1 - num_2
multiply = num_1 * num_2
devide = num_1 // num_2

print(num_1, "+", num_2, "=", plus)
print(num_1, "-", num_2, "=", minus)
print(num_1, "*", num_2, "=", multiply)
print(num_1, "/", num_2, "=", devide)
